  let myButton = document.getElementById("searchButton");
  myButton.addEventListener("click", (event) => { 
    
    let inputJob = document.getElementById("jobTitle").value;
    let inputLocation = document.getElementById("location").value;
    inputJob = jobs.title;
    inputLocation = jobs.location;
    let objectResult = [];
    
    for (let i = 0; i < jobs.length; i++){
      let job = jobs[i];
      let newInputJob = job.title.toLowerCase();
      let newInputLocation = job.location.toLowerCase();
      
      if ((newInputJob.includes(inputJob)) && (newInputLocation.includes(inputLocation))) {
        
        let result = {
            title: job.title,
            location: job.location
        };
        objectResult.push(result);
      }
      
    }
    return {
        result: objectResult,
        count: objectResult.length
    };
   
  });
  
